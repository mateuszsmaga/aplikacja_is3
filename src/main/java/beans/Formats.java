package beans;

public class Formats {
	private String xml;
	private String yaml;
	private String ogdl;
	private String json;
	public String getXml() {
		return xml;
	}
	public void setXml(String xml) {
		this.xml = xml;
	}
	public String getYaml() {
		return yaml;
	}
	public void setYaml(String yaml) {
		this.yaml = yaml;
	}
	public String getOgdl() {
		return ogdl;
	}
	public void setOgdl(String ogdl) {
		this.ogdl = ogdl;
	}
	public String getJson() {
		return json;
	}
	public void setJson(String json) {
		this.json = json;
	}
	
	
	

	
	
}
